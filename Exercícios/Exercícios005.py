'''
5 - Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.
'''

n = int(input('Digite um número natural: ')) #Aqui será guardado o número que o usuário digitar.

print(f'Sucessor: {n+1}') #Será somado +1 no número que o usuário digitou.
print(f'Antecessor: {n-1}') #Será somado -1 no número que o usuário digitou.